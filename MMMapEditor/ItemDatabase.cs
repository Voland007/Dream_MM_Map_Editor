        }

        public static ItemInfo GetItemByIndex(int index)
        {
            if (_itemsById == null)
                ParseItemData();

            _itemsById.TryGetValue(index, out var item);
            return item;
        }

        public static string GetItemName(int index)
        {
            var item = GetItemByIndex(index);
            return item?.Name?.Trim() ?? $"Item {index}";
        }

        public static bool TryGetItemName(int index, out string itemName)
        {
            var item = GetItemByIndex(index);
            if (item != null)
            {
                itemName = item.Name?.Trim();
                return true;
            }

            itemName = null;
            return false;

        private static ItemInfo ParseItemEntry(byte[] data, int index)
        {
            var item = new ItemInfo
            {
                RawData = data,
                Index = index
            };

            int nameLength = 0;
            for (int i = 0; i < 16; i++)
            {
                if (data[i] == 0)
                    break;
                nameLength++;
            }

            item.Name = nameLength > 0
                ? Encoding.ASCII.GetString(data, 0, nameLength).Trim()
                : $"Item_{index}";

            return item;
        }
